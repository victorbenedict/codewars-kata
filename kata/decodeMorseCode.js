const decodeMorse = function (morseCode) {
  return morseCode
    .trim()
    .split('   ')
    .map((arr) =>
      arr.split(' ').reduce((sentence, word) => {
        return sentence + MORSE_CODE[word];
      }, '')
    )
    .join(' ');
};
